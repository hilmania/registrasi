function chara = getChara(num)

switch(num)
    case 1
        chara='A';
    case 2
        chara='B';
    case 3
        chara='C';
    case 4
        chara='D';
    case 5
        chara='E';
    case 6
        chara='F';
    case 7
        chara='G';
    case 8
        chara='H';
    case 9
        chara='I';
    case 10
        chara='J';
    otherwise
        chara='N/A';
end
